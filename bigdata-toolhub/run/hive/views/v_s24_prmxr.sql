use zhgl;

CREATE VIEW if not exists V_S24_PRMXR AS
SELECT BANK,
          CURR_NUM,
          EFFECT_DAY,
          RATE_VAL0 / 10000000 AS RATE_VAL0,
          RATE_VAL1 / 10000000 AS RATE_VAL1,
          RATE_VAL2 / 10000000 AS RATE_VAL2,
          RATE_VAL3 / 10000000 AS RATE_VAL3,
          RATE_VAL4 / 10000000 AS RATE_VAL4,
          RATE_VAL5 / 10000000 AS RATE_VAL5,
          RATE_VAL6 / 10000000 AS RATE_VAL6
     FROM S24_PRMXR;

